<template>
  <div>
    <div id="plan_upload" v-if="noPlan">
      <plan-upload @upload="handlePlanUpload" />
    </div>

    <div v-else>
      <div id="floor">
        <div
          v-drag
          v-zoom
          id="plan"
          :key="planKey"
          :style="
            centerSwitch
              ? {
                  transform: `translate(${-(
                    centerSwitch.positionLeft +
                    windowWidth / 2
                  )}px, ${
                    -(centerSwitch.positionTop + windowHeight / 2) - 64
                  }px) scale(1)`,
                }
              : {}
          "
        >
          <v-img :src="planPath" class="image" @error="noPlan = true"></v-img>

          <v-chip
            v-for="sw in switches"
            v-drag-switch="sw"
            :key="sw.name"
            :id="sw.name"
            dark
            class="switch ma-2"
            :style="
              sw.positionTop && sw.positionLeft
                ? {
                    top: sw.positionTop + 'px',
                    left: sw.positionLeft + 'px',
                  }
                : { display: 'none' }
            "
          >
            {{ sw.name }}
          </v-chip>
        </div>

        <v-toolbar floating>
          <v-select
            v-model="swName"
            :items="switchesWithoutPosition"
            hide-details
            item-text="name"
            item-value="name"
            label="Switch"
            color="orange accent-2"
          ></v-select>
          <v-btn
            v-if="swName !== ''"
            class="ma-1"
            color="orange darken-1"
            @click="place(swName)"
          >
            Place
          </v-btn>
        </v-toolbar>
      </div>

      <v-btn
        rounded
        large
        fixed
        bottom
        right
        color="orange accent-4"
        dark
        @click="openSwitchForm('Add', undefined, shortName, parseInt(floor))"
      >
        Add switch
      </v-btn>

      <SwitchForm
        :form="switchForm"
        :action="switchFormAction"
        :needLocationFields="false"
        :sw="sw"
        @submit="handleSubmitSwitch"
        @close="closeSwitchForm"
      />

      <snackbar
        :snackbar="snackbar"
        :type="snackbarType"
        :text="snackbarText"
        @close="closeSnackbar"
      />
    </div>
  </div>
</template>

<script lang="ts">
import { defineComponent, ref, Ref } from "@vue/composition-api";
import { mdiMagnify, mdiPlus } from "@mdi/js";

import drag from "@/directives/drag";
import dragSwitch from "@/directives/dragSwitch";
import zoom from "@/directives/zoom";

import PlanUpload from "@/components/PlanUpload.vue";
import SwitchForm from "@/components/forms/SwitchForm.vue";
import Snackbar from "@/components/Snackbar.vue";

import { SwitchResponse } from "@/types/switch";
import { getSwitchesOfFloor } from "@/api/switches";
import { getPlan, uploadPlan } from "@/api/plans";

import useSwitchForm from "@/composables/useSwitchForm";
import useSnackbar from "@/composables/useSnackbar";

export default defineComponent({
  props: {
    isLoading: { type: Boolean, required: true },
    shortName: { type: String, required: true },
    floor: { type: String, required: true },
  },

  components: {
    PlanUpload,
    SwitchForm,
    Snackbar,
  },

  directives: {
    drag,
    dragSwitch,
    zoom,
  },

  setup() {
    const planPath = ref("");

    const noPlan = ref(false);

    const planKey = ref(0);

    const {
      form: switchForm,
      formAction: switchFormAction,
      sw,
      openForm: openSwitchForm,
      submitForm: submitSwitchForm,
      closeForm: closeSwitchForm,
    } = useSwitchForm();

    const {
      snackbar,
      snackbarType,
      text: snackbarText,
      open: openSnackbar,
      close: closeSnackbar,
    } = useSnackbar();

    const swName = ref("");
    const switches: Ref<SwitchResponse[]> = ref([]);
    const switchesWithoutPosition: Ref<SwitchResponse[]> = ref([]);

    const centerSwitchName: Ref<string | undefined> = ref();
    const centerSwitch: Ref<SwitchResponse | undefined> = ref();
    const windowWidth = window.innerWidth;
    const windowHeight = window.innerHeight;

    return {
      planPath,
      noPlan,
      planKey,

      uploadPlan,

      swName,
      switches,
      switchesWithoutPosition,

      centerSwitchName,
      centerSwitch,
      windowWidth,
      windowHeight,

      // Switch form.
      switchForm,
      switchFormAction,
      sw,
      openSwitchForm,
      submitSwitchForm,
      closeSwitchForm,

      // Snackbar.
      snackbar,
      snackbarType,
      snackbarText,
      openSnackbar,
      closeSnackbar,

      mdiMagnify,
      mdiPlus,
    };
  },

  methods: {
    async displaySwitches() {
      const sws = await getSwitchesOfFloor(
        this.shortName,
        parseInt(this.floor)
      );
      sws.forEach((sw) => {
        this.switches.push(sw);
        if (!sw.positionTop && !sw.positionLeft) {
          this.switchesWithoutPosition.push(sw);
        }
      });
      this.planKey += 1;
    },

    showPlan() {
      getPlan(`/plans/${this.shortName}f${this.floor}.png`)
        .then((uri) => {
          if (uri) {
            this.planPath = uri;
            this.noPlan = false;
          } else {
            this.noPlan = true;
          }
        })
        .catch(() => {
          this.noPlan = true;
        });
    },

    handlePlanUpload(plan: File) {
      uploadPlan(this.shortName, parseInt(this.floor), plan).then(() =>
        this.showPlan()
      );
    },

    place(name: string) {
      const switchToPlace = this.switchesWithoutPosition.find((sw) => {
        return sw.name == name;
      });

      if (switchToPlace) {
        this.switchesWithoutPosition = this.switchesWithoutPosition.filter(
          (sw) => sw.name !== name
        );

        this.swName = "";

        const plan = document.getElementById("plan");
        if (plan) {
          switchToPlace.positionTop = plan.offsetHeight / 2;
          switchToPlace.positionLeft = plan.offsetWidth / 2;
        }
      }
    },

    async handleSubmitSwitch(
      name: string,
      ipResolveMethod: string,
      ip: string,
      mac: string,
      upSwitchName: string,
      upLink: string,
      snmpCommunity: string,
      revision: string,
      serial: string,
      build: string,
      floor: number,
      retrieveFromNetData: boolean,
      retrieveUpLinkFromSeens: boolean,
      retrieveTechDataFromSNMP: boolean,
      action: "Add" | "Edit"
    ) {
      try {
        await this.submitSwitchForm(
          name,
          ipResolveMethod,
          ip,
          mac,
          upSwitchName,
          upLink,
          snmpCommunity,
          revision,
          serial,
          build,
          floor,
          retrieveFromNetData,
          retrieveUpLinkFromSeens,
          retrieveTechDataFromSNMP,
          action
        );
        this.displaySwitches();
        this.openSnackbar(
          "success",
          `${name} succesfully ${action.toLowerCase()}ed`
        );
      } catch (err: unknown) {
        this.openSnackbar("error", `Failed to ${action.toLowerCase()} switch`);
      }
    },
  },

  created() {
    const centerSwitch = this.$route.query.sw;
    this.centerSwitchName =
      typeof centerSwitch === "object" ? undefined : centerSwitch;
    console.log(this.centerSwitchName);
    this.showPlan();
    this.displaySwitches().then(() => {
      console.log(this.switches);
      this.centerSwitch = this.switches.find(
        (sw) => sw.name === this.centerSwitchName
      );
      console.log(this.centerSwitch);
    });
  },
});
</script>

